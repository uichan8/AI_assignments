    true_score = x[np.arange(x.shape[0]),y].reshape(-1,1)
    margin = x - true_score + 1
    margin *= (margin > 0)
    margin[np.arange(x.shape[0]),y] = 0
    loss = margin.sum() / x.shape[0]

    margin = (margin > 0).astype(int)
